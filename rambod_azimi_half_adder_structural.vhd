library IEEE;
use IEEE.STD_LOGIC_1164.ALL;


entity rambod_azimi_half_adder_structural is
port (a: in std_logic;
		b: in std_logic;
		s: out std_logic;
		c: out std_logic);
end rambod_azimi_half_adder_structural;


--Architecture (Structural)
architecture halfadder of rambod_azimi_half_adder_structural is
begin

s <= a xor b;
c <= a and b;

end halfadder;